package pua.net.dto;

public class SubtemaDTO {
	private int idTema;
	private int idSubtema;
	private int numeroSubtema;
	private String subtema;
	
	public int getIdTema() {
		return idTema;
	}
	public void setIdTema(int idTema) {
		this.idTema = idTema;
	}
	public int getIdSubtema() {
		return idSubtema;
	}
	public void setIdSubtema(int idSubtema) {
		this.idSubtema = idSubtema;
	}
	public int getNumeroSubtema() {
		return numeroSubtema;
	}
	public void setNumeroSubtema(int numeroSubtema) {
		this.numeroSubtema = numeroSubtema;
	}
	public String getSubtema() {
		return subtema;
	}
	public void setSubtema(String subtema) {
		this.subtema = subtema;
	}
	
}
