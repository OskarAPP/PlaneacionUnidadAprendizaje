package pua.net.dto;

public class GenericaDTO {
	private int idGenerica;
	private int idPua;
	private String nombreGenerica;
	
	public int getIdGenerica() {
		return idGenerica;
	}
	public void setIdGenerica(int idGenerica) {
		this.idGenerica = idGenerica;
	}
	public String getNombreGenerica() {
		return nombreGenerica;
	}
	public void setNombreGenerica(String nombreGenerica) {
		this.nombreGenerica = nombreGenerica;
	}
	public int getIdPua() {
		return idPua;
	}
	public void setIdPua(int idPua) {
		this.idPua = idPua;
	}
	
	
}
